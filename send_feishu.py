    if len(chats) == 0:
        print("❌ 没有找到任何群聊")
        sys.exit(1)
    
    # 获取日期类型
    day_type = get_day_type()
    
    # 根据日期类型选择文案
    if day_type == "weekend":
        print("📅 今天是周末（周六/周日）")
        messages = WEEKEND_MESSAGES
        is_weekend = True
    elif day_type == "friday":
        print("📅 今天是周五")
        messages = FRIDAY_MESSAGES
        is_weekend = False
    else:
        print(f"📅 今天是工作日（周一到周四）")
        messages = WORKDAY_MESSAGES
        is_weekend = False
    
    # 随机选择一条消息
    message = random.choice(messages)
